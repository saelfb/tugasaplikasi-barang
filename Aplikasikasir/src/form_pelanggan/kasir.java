    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        String ID = id.getText();
        String NAMA = nama.getText();
        String kelamin = blaki.isSelected() ? "Laki Laki" : "Perempuan";
        String telp = telepon.getText();
        String Alamat = alamat.getText();

try {
    Connection c = koneksi.koneksi.getkoneksi();
    String sql = "INSERT INTO kasir (id_kasir, nama_kasir, jenis_kelamin, no_telpon, alamat) VALUES (?, ?, ?, ?, ?)";
    PreparedStatement p = c.prepareStatement(sql);
    p.setString(1, ID);
    p.setString(2, NAMA);
    p.setString(3, kelamin);
    p.setString(4, telp);
    p.setString(5, Alamat);
    p.executeUpdate();
    JOptionPane.showMessageDialog(this, "Data berhasil disimpan");
    resetForm();
    loadTabel();
} catch (SQLException e) {
    JOptionPane.showMessageDialog(this, "Gagal simpan data: " + e.getMessage());
}
    }//GEN-LAST:event_saveActionPerformed

