    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        // TODO add your handling code here:
        String ID = id.getText();

try {
    Connection c = koneksi.koneksi.getkoneksi();
    String sql = "DELETE FROM kasir WHERE id_kasir=?";
    PreparedStatement p = c.prepareStatement(sql);
    p.setString(1, ID);
    p.executeUpdate();
    JOptionPane.showMessageDialog(this, "Data berhasil dihapus");
    resetForm();
    loadTabel();
} catch (SQLException e) {
    JOptionPane.showMessageDialog(this, "Gagal hapus data: " + e.getMessage());
}}
private void loadTabel() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID");
    model.addColumn("Nama");
    model.addColumn("Jenis Kelamin");
    model.addColumn("Username");
    model.addColumn("Password");
    model.addColumn("Telepon");
    model.addColumn("Alamat");

    try {
        Connection c = koneksi.koneksi.getkoneksi();
        Statement s = c.createStatement();
        ResultSet r = s.executeQuery("SELECT * FROM kasir");

        while (r.next()) {
            model.addRow(new Object[]{
                r.getString("id_kasir"),
                r.getString("nama_kasir"),
                r.getString("jenis_kelamin"),
                r.getString("username"),
                r.getString("password"),
                r.getString("no_telpon"),
                r.getString("alamat")
            });
        }

        kasirtbl.setModel(model);
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Gagal load tabel: " + e.getMessage());
    }
}

