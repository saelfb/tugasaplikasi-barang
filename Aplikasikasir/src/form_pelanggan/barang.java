    private void searchbActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchbActionPerformed
        // TODO add your handling code here:
        String keyword = search.getText(); // Ambil teks dari input cari

    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("kode_barang");
    model.addColumn("nama_barang");
    model.addColumn("jenis_barang");
    model.addColumn("harga_beli");
    model.addColumn("harga_jual");

    try {
        String sql = "SELECT * FROM barang WHERE nama_barang LIKE ?";
        Connection con = koneksi.koneksi.getkoneksi();
        PreparedStatement pst = con.prepareStatement(sql);
        pst.setString(1, "%" + keyword + "%");

        ResultSet rs = pst.executeQuery();

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("KODE_BARANG"),
                rs.getString("NAMA_BARANG"),
                rs.getString("JENIS_BARANG"),
                rs.getString("HARGA_BELI"),
                rs.getString("HARGA_JUAL")
            });
        }

        tbl.setModel(model); // Tampilkan hasil ke tabel

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
}
    }//GEN-LAST:event_searchbActionPerformed

