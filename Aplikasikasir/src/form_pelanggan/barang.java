    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        String kbarang = KD.getText();
        String idbarang = ID.getText();
        String jbarang = JB.getSelectedItem().toString();
        String hb = HB.getText();
        String hj = HJ.getText();

    try {
        Connection con = koneksi.koneksi.getkoneksi();
        String sql = "INSERT INTO barang (kode_barang, nama_barang, jenis_barang, harga_beli, harga_jual) VALUES (?, ?, ?, ?, ?)";
        PreparedStatement pst = con.prepareStatement(sql);
        pst.setString(1, kbarang);
        pst.setString(2, idbarang);
        pst.setString(3, jbarang);
        pst.setString(4, hb);
        pst.setString(5, hj);
        pst.executeUpdate();
        JOptionPane.showMessageDialog(this, "Data berhasil disimpan.");
        loadTabel(); // fungsi untuk refresh table
        resetForm();
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Gagal simpan data: " + e.getMessage());
    }
    }//GEN-LAST:event_saveActionPerformed

    private void changeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_changeActionPerformed
        // TODO add your handling code here:
        try {
        String sql = "UPDATE barang SET nama_barang=?, jenis_barang=?, harga_beli=?, harga_jual=? WHERE kode_barang=?";
        Connection conn = koneksi.koneksi.getkoneksi(); // pastikan koneksi sukses
        PreparedStatement pst = conn.prepareStatement(sql);

        pst.setString(1, ID.getText());
        pst.setString(2, JB.getSelectedItem().toString());
        pst.setString(3, HB.getText());
        pst.setString(4, HJ.getText());
        pst.setString(5, KD.getText()); // <-- pastikan ini tidak kosong

        int rowsAffected = pst.executeUpdate();

        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(null, "Data berhasil diubah!");
            loadTabel(); // refresh tabel
        } else {
            JOptionPane.showMessageDialog(null, "Kode tidak ditemukan atau tidak ada perubahan data.");
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Gagal mengubah data: " + e.getMessage());
}
    }//GEN-LAST:event_changeActionPerformed

